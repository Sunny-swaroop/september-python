"""
end and sep are parameters used with the print() function to control the output format.
the end parameter in the print() function specifies what should be printed at the end of the line.
By default, print() ends with a newline character (\n), so each call to print() starts on a new line.
The sep parameter in the print() function specifies the separator between multiple items when you pass them to print().
By default, sep is a space (' ').
"""
"end example"
print("Hello", end=' ')
print("world!")
"sep example"
print("apple", "banana", "cherry", sep=', ')

