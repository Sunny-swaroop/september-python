#formating function using "f"
#multiplication of  two numbers
a=int(input("enter the value of a :"))
b=int(input("enter the value of b :")) 
c=a*b
print(f"the value of {a} * {b} = {c}")
#it is one type of formating function
#by keeping "f" as before the statement we can format,"{}" these are called place holders
#with the help of place holders it can directly paste the value of variable 
