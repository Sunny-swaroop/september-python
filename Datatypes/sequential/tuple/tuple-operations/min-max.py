#minimum and maximum functions
#these operations can show the minimum value of the element present in tuple
#and also maximum value of the element present in the tuple
#example
tuple1=(10,2,5,100,90,80)
print(tuple1)
print("the minimum number in given tuple is :", min(tuple1)) #it will display the element "2"
print("the maximum number in given tuple is :",max(tuple1)) #it will display the element "100"
